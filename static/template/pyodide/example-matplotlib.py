
import matplotlib.pyplot as plt
import numpy as np

# –ì—Ä–∞—Ñ–∏–∫ 2. –ö–∞—á–µ—Å—Ç–≤–æ –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º
categories = [
    "–ü–ª–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ", "–ê–Ω–∞–ª–∏–∑", "–ü—Ä–æ–µ–∫—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ", "–ò–Ω—Ñ—Ä–∞—Å—Ç—Ä—É–∫—Ç—É—Ä–∞", "–†–∞–∑—Ä–∞–±–æ—Ç–∫–∞", 
    "–ò–Ω—Ç–µ–≥—Ä–∞—Ü–∏—è", "–ò—Å–ø—Ä–∞–≤–ª–µ–Ω–∏—è", "–¢–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ", "–î–æ–∫—É–º–µ–Ω—Ç–∞—Ü–∏—è", "–û–±—É—á–µ–Ω–∏–µ", "–í–Ω–µ–¥—Ä–µ–Ω–∏–µ"
]
means = [95, 88, 92, 90, 90.2, 85, 88, 89.5, 92, 94, 88]  # –≤—Ä—É—á–Ω—É—é/—Å—Ä–µ–¥–Ω–∏–µ

colors = []
for score in means:
    if score >= 90:
        colors.append("#4CAF50")
    elif score >= 80:
        colors.append("#FFA500")
    else:
        colors.append("#F44336")

plt.figure(figsize=(8,4))
plt.bar(categories, means, color=colors)
plt.title("–°—Ä–µ–¥–Ω–µ–µ –∫–∞—á–µ—Å—Ç–≤–æ –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º –ø—Ä–æ–µ–∫—Ç–∞")
plt.ylabel("–ö–∞—á–µ—Å—Ç–≤–æ")
plt.ylim(80, 100)
plt.xticks(rotation=30)
plt.grid(axis='y', alpha=0.3)
plt.tight_layout()
plt.show()

#%copy_console="–ü—Ä–æ–∞–Ω–∞–ª–∏–∑–∏—Ä—É–π –¥–∞–Ω–Ω—ã–µ –Ω–∏–∂–µ –∏ —Å–¥–µ–ª–∞–π –∫—Ä–∞—Ç–∫–∏–µ –≤—ã–≤–æ–¥—ã"
# #%send_console="–ü—Ä–æ–∞–Ω–∞–ª–∏–∑–∏—Ä—É–π –¥–∞–Ω–Ω—ã–µ –Ω–∏–∂–µ –∏ —Å–¥–µ–ª–∞–π –∫—Ä–∞—Ç–∫–∏–µ –≤—ã–≤–æ–¥—ã"
# –í—ã–≤–æ–¥ –¥–∞–Ω–Ω—ã—Ö –≤ –∫–æ–Ω—Å–æ–ª—å –¥–ª—è –∞–Ω–∞–ª–∏–∑–∞
print("=== –î–ê–ù–ù–´–ï –î–õ–Ø –ê–ù–ê–õ–ò–ó–ê –ì–†–ê–§–ò–ö–ê ===")
print("–ü–µ—Ä–µ–¥–∞—é—Ç—Å—è –¥–∞–Ω–Ω—ã–µ –æ –∫–∞—á–µ—Å—Ç–≤–µ —Ä–∞–±–æ—Ç –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º –ø—Ä–æ–µ–∫—Ç–∞ –¥–ª—è –ø–æ–¥–≥–æ—Ç–æ–≤–∫–∏ –∞–Ω–∞–ª–∏—Ç–∏—á–µ—Å–∫–∏—Ö –≤—ã–≤–æ–¥–æ–≤:")
print()

for i, (category, score) in enumerate(zip(categories, means)):
    status = "üü¢ –û—Ç–ª–∏—á–Ω–æ" if score >= 90 else "üü° –•–æ—Ä–æ—à–æ" if score >= 80 else "üî¥ –¢—Ä–µ–±—É–µ—Ç –≤–Ω–∏–º–∞–Ω–∏—è"
    print(f"{i+1:2d}. {category:<15}: {score:5.1f} - {status}")

print()
print(f"–û–±—â–∞—è —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞:")
print(f"- –°—Ä–µ–¥–Ω–∏–π –±–∞–ª–ª: {np.mean(means):.1f}")
print(f"- –ú–∞–∫—Å–∏–º—É–º: {max(means):.1f} ({categories[means.index(max(means))]})")
print(f"- –ú–∏–Ω–∏–º—É–º: {min(means):.1f} ({categories[means.index(min(means))]})")
print(f"- –ö–∞—Ç–µ–≥–æ—Ä–∏–π —Å –æ—Ü–µ–Ω–∫–æ–π ‚â•90: {sum(1 for x in means if x >= 90)}")
print(f"- –ö–∞—Ç–µ–≥–æ—Ä–∏–π —Ç—Ä–µ–±—É—é—â–∏—Ö –≤–Ω–∏–º–∞–Ω–∏—è (<80): {sum(1 for x in means if x < 80)}")